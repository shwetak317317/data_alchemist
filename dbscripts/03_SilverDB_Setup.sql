-- ============================================================
-- fact_sales
-- Grain: one row per order line item
-- Degenerate dimensions: SourceOrderID, SourceLineItemID
-- ============================================================
CREATE TABLE dbo.fact_sales (
    SalesKey          INT            IDENTITY(1,1) PRIMARY KEY,
    -- dimensions
    OrderDateKey      INT            NULL,   -- FK dim_date
    CustomerKey       INT            NULL,   -- FK dim_customer
    ProductKey        INT            NULL,   -- FK dim_product
    CategoryKey       INT            NULL,   -- FK dim_category
    LocationKey       INT            NULL,   -- FK dim_location
    PromoKey          INT            NULL,   -- FK dim_promotion (nullable)
    PaymentMethodKey  INT            NULL,   -- FK dim_payment_method
    -- degenerate
    SourceSystem      VARCHAR(30)    NOT NULL,
    SourceOrderID     INT            NULL,
    SourceLineItemID  INT            NULL,
    OrderStatus       VARCHAR(30)    NULL,
    -- measures
    Quantity          INT            NULL,
    UnitPrice         DECIMAL(10,2)  NULL,
    LineTotal         DECIMAL(10,2)  NULL,
    Discount          DECIMAL(10,2)  NULL,
    GrossAmount       DECIMAL(10,2)  NULL,   -- from order header
    TaxAmount         DECIMAL(10,2)  NULL,
    ShippingAmount    DECIMAL(10,2)  NULL,
    NetPayable        DECIMAL(10,2)  NULL,
    AmountPaid        DECIMAL(10,2)  NULL,   -- from payments
    LineDiscrepancy   BIT            NULL,   -- from bronze flag
    IsFulfilled       BIT            NULL,
    -- audit
    Slv_LoadedAt      DATETIME       NOT NULL DEFAULT GETDATE(),
    Slv_BatchID       INT            NOT NULL
);
GO
CREATE INDEX IX_FactSales_DateKey     ON dbo.fact_sales (OrderDateKey);
CREATE INDEX IX_FactSales_CustomerKey ON dbo.fact_sales (CustomerKey);
CREATE INDEX IX_FactSales_ProductKey  ON dbo.fact_sales (ProductKey);
GO

-- ============================================================
-- fact_returns
-- Grain: one row per return event
-- ============================================================
CREATE TABLE dbo.fact_returns (
    ReturnKey         INT            IDENTITY(1,1) PRIMARY KEY,
    ReturnDateKey     INT            NULL,   -- FK dim_date
    CustomerKey       INT            NULL,   -- FK dim_customer (via order)
    ProductKey        INT            NULL,   -- FK dim_product (via SKU/BarCode lookup)
    WarehouseKey      INT            NULL,   -- FK dim_warehouse
    -- degenerate
    SourceSystem      VARCHAR(30)    NOT NULL,
    SourceReturnID    INT            NULL,
    SourceOrderID     INT            NULL,
    SourceShipmentID  INT            NULL,
    -- measures
    ReturnQty         INT            NULL,
    RefundAmount      DECIMAL(10,2)  NULL,
    ReturnReason      VARCHAR(100)   NULL,
    ReturnStatus      VARCHAR(30)    NULL,
    IsRefunded        BIT            NULL,
    -- audit
    Slv_LoadedAt      DATETIME       NOT NULL DEFAULT GETDATE(),
    Slv_BatchID       INT            NOT NULL
);
GO
CREATE INDEX IX_FactReturns_DateKey ON dbo.fact_returns (ReturnDateKey);
GO

-- ============================================================
-- fact_inventory_snapshot
