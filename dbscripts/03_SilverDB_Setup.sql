-- ============================================================
--  03_SilverDB_Setup.sql
--  Analytics-ready star schema.
--
--  SURROGATE KEY DESIGN:
--    Every Silver table has an IDENTITY surrogate key (e.g. CustomerKey).
--    The natural key in the warehouse is SourceSystem + SourceNativeID.
--    Example: CustomerID=1001 from RetailMart -> CustomerKey = new INT
--             CustomerID=2001 from LogiShip   -> CustomerKey = new INT
--    This guarantees uniqueness across both sources with no collision.
--
--  SCD TYPE 2 on dim_customer and dim_product:
--    IsCurrent, ValidFrom, ValidTo columns track history.
--
--  dim_date is generated (spine 2018-01-01 to 2030-12-31).
-- ============================================================

USE master;
GO
IF EXISTS (SELECT 1 FROM sys.databases WHERE name = 'SilverDB')
BEGIN
    ALTER DATABASE SilverDB SET SINGLE_USER WITH ROLLBACK IMMEDIATE;
    DROP DATABASE SilverDB;
END
GO
CREATE DATABASE SilverDB COLLATE SQL_Latin1_General_CP1_CI_AS;
GO
USE SilverDB;
GO

-- ============================================================
-- CONTROL TABLE
-- ============================================================
CREATE TABLE dbo.SilverLoadLog (
    BatchID        INT          IDENTITY(1,1) PRIMARY KEY,
    BronzeBatchID  INT          NOT NULL,
    RunStartedAt   DATETIME     NOT NULL DEFAULT GETDATE(),
    RunFinishedAt  DATETIME     NULL,
    Status         VARCHAR(20)  NOT NULL DEFAULT 'Running',
    Notes          VARCHAR(500) NULL
);
GO

-- ============================================================
-- dim_date  (static calendar spine — generated by SP)
-- ============================================================
CREATE TABLE dbo.dim_date (
    DateKey        INT          NOT NULL PRIMARY KEY,   -- YYYYMMDD integer
    FullDate       DATE         NOT NULL UNIQUE,
    DayOfWeek      TINYINT      NOT NULL,               -- 1=Sun..7=Sat
    DayName        VARCHAR(10)  NOT NULL,
    DayOfMonth     TINYINT      NOT NULL,
    DayOfYear      SMALLINT     NOT NULL,
    WeekOfYear     TINYINT      NOT NULL,
    MonthNumber    TINYINT      NOT NULL,
    MonthName      VARCHAR(10)  NOT NULL,
    Quarter        TINYINT      NOT NULL,
    Year           SMALLINT     NOT NULL,
    IsWeekend      BIT          NOT NULL,
    IsHoliday      BIT          NOT NULL DEFAULT 0,
    FiscalYear     SMALLINT     NOT NULL,               -- Apr-Mar Indian FY
    FiscalQuarter  TINYINT      NOT NULL
);
GO

-- ============================================================
-- dim_customer  (SCD Type 2)
-- Natural key: SourceSystem + SourceCustomerID
-- ============================================================
CREATE TABLE dbo.dim_customer (
    CustomerKey       INT           IDENTITY(1,1) PRIMARY KEY,
    SourceSystem      VARCHAR(30)   NOT NULL,
    SourceCustomerID  INT           NOT NULL,
    FirstName         VARCHAR(100)  NULL,
    LastName          VARCHAR(100)  NULL,
    FullName          VARCHAR(205)  NULL,               -- derived: FirstName + ' ' + LastName
    Email             VARCHAR(255)  NULL,
    Phone             VARCHAR(20)   NULL,
    DateOfBirth       DATE          NULL,
    AgeYears          INT           NULL,               -- derived at load time
    Gender            VARCHAR(10)   NULL,               -- standardised: Male/Female/Other/Unknown
    LoyaltyTier       VARCHAR(20)   NULL,               -- standardised: Bronze/Silver/Gold/Platinum/Unknown
    RegisteredDate    DATETIME      NULL,
    IsActive          BIT           NULL,
    CountryCode       CHAR(2)       NULL,
    -- SCD2
    IsCurrent         BIT           NOT NULL DEFAULT 1,
    ValidFrom         DATETIME      NOT NULL DEFAULT GETDATE(),
    ValidTo           DATETIME      NULL,               -- NULL = current record
    -- audit
    Slv_LoadedAt      DATETIME      NOT NULL DEFAULT GETDATE(),
    Slv_BatchID       INT           NOT NULL,
    CONSTRAINT UQ_DimCustomer_Current UNIQUE (SourceSystem, SourceCustomerID, IsCurrent)
);
GO
CREATE INDEX IX_DimCustomer_NatKey ON dbo.dim_customer (SourceSystem, SourceCustomerID, IsCurrent);
GO

-- ============================================================
-- dim_product  (SCD Type 2)
-- Natural key: SourceSystem + SourceProductID
-- ============================================================
CREATE TABLE dbo.dim_product (
    ProductKey        INT            IDENTITY(1,1) PRIMARY KEY,
    SourceSystem      VARCHAR(30)    NOT NULL,
    SourceProductID   INT            NOT NULL,
    SKU_Barcode       VARCHAR(50)    NULL,
    ProductName       VARCHAR(255)   NULL,
    CategoryKey       INT            NULL,              -- FK -> dim_category
    BasePrice         DECIMAL(10,2)  NULL,
    CostPrice         DECIMAL(10,2)  NULL,
    GrossMarginPct    DECIMAL(6,2)   NULL,              -- derived: (BasePrice-CostPrice)/BasePrice*100
    StockQty          INT            NULL,
    Weight_kg         DECIMAL(10,4)  NULL,
    IsActive          BIT            NULL,
    LaunchDate        DATE           NULL,
    -- SCD2
    IsCurrent         BIT            NOT NULL DEFAULT 1,
    ValidFrom         DATETIME       NOT NULL DEFAULT GETDATE(),
    ValidTo           DATETIME       NULL,
    -- audit
    Slv_LoadedAt      DATETIME       NOT NULL DEFAULT GETDATE(),
    Slv_BatchID       INT            NOT NULL,
    CONSTRAINT UQ_DimProduct_Current UNIQUE (SourceSystem, SourceProductID, IsCurrent)
);
GO
CREATE INDEX IX_DimProduct_NatKey ON dbo.dim_product (SourceSystem, SourceProductID, IsCurrent);
GO

-- ============================================================
-- dim_category  (SCD Type 1 — simple overwrite)
-- Natural key: SourceSystem + SourceCategoryID
-- ============================================================
CREATE TABLE dbo.dim_category (
    CategoryKey       INT           IDENTITY(1,1) PRIMARY KEY,
    SourceSystem      VARCHAR(30)   NOT NULL,
